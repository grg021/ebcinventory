<?php

class BaseController extends Controller {

	protected $limit = 30;

	/**
	 * Setup the layout used by the controller.
	 *
	 * @return void
	 */
	protected function setupLayout()
	{
		if ( ! is_null($this->layout))
		{
			$this->layout = View::make($this->layout);
		}
	}

	public function getLimit() {
		return $this->limit;
	}

}
